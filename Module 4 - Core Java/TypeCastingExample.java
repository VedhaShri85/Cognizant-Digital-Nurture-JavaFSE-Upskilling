public class TypeCastingExample {

    public static void main(String[] args) {

        double d = 15.75;

        int i = (int)d;

        System.out.println("Double Value = " + d);
        System.out.println("Integer Value = " + i);

        int num = 25;

        double result = (double)num;

        System.out.println("Converted Double = " + result);

    }

}